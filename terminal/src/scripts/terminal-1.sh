# En aquesta demostració veurem què és i com s'utilitza un terminal.
#
# En el terminal, les línies que comencen per '#' son comentaris.
# Utilitzem els comentaris en aquestes lliçons per explicar-vos
# el que fem.

#
# Comencem: El terminal (o consola) és un programa del
# sistema operatiu que executa les nostres comandes.
#
# Cada comanda es correspon a un programa, que podem executar
# escrivint el seu nom en el terminal.
#
# Per exemple, el programa 'date' mostra la data i l'hora actual:
#

date


#
# El programa 'cal' mostra el calendari del mes actual:
#

cal


#
# Molts programes admeten paràmetres optatius per modificar
# o ampliar el seu funcionament. Per exemple, 'cal' accepta un
# mes i un any com a paràmetres. Per tant, si volem veure el
# calendari del setembre de 1714 podem executar
#

cal 9 1714


#
# El programa 'calc' és una calculadora senzilla:
#

calc '2 + 2'
calc '1 + 10 * 3'
calc '(2 + 10) / 3'


#
# En aquest cas, els paràmetres s'han tancat entre cometes
# per tal que el terminal no interpreti els símbols especials
# com ara els operadors i els parèntesis.
#


